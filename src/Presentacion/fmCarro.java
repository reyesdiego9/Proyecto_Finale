    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion="editar";
        
        int fila = tablalistado.rowAtPoint(evt.getPoint());
        txtidecarro.setText(tablalistado.getValueAt(fila, 0).toString());
        
        txtPlaca.setText(tablalistado.getValueAt(fila, 1).toString());
        txtMarca.setText(tablalistado.getValueAt(fila, 1).toString());
        txtModelo.setText(tablalistado.getValueAt(fila, 3).toString());
        txtMotor.setText(tablalistado.getValueAt(fila, 4).toString());
        txtCilindro.setText(tablalistado.getValueAt(fila, 5).toString());
        txtColor.setText(tablalistado.getValueAt(fila, 6).toString());
        txtAnio.setText(tablalistado.getValueAt(fila, 7).toString());
        txtPuerta.setText(tablalistado.getValueAt(fila, 8).toString());
        JTipo.setSelectedItem(tablalistado.getValueAt(fila, 9).toString());
        JEstado.setSelectedItem(tablalistado.getValueAt(fila, 10).toString());
        txtPrecio.setText(tablalistado.getValueAt(fila, 11).toString());
        txtdesc.setText(tablalistado.getValueAt(fila, 12).toString());
        
    }//GEN-LAST:event_tablalistadoMouseClicked

