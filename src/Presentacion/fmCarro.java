    private void btguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btguardarActionPerformed
        // TODO add your handling code here:
        if(txtPlaca.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar placa del vehiculo");
            txtPlaca.requestFocus();
            return;
        }
        if(txtMarca.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar marca del vehiculo");
            txtMarca.requestFocus();
            return;
        }
        if(txtModelo.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar modelo del vehiculo");
            txtModelo.requestFocus();
            return;
        }
        if(txtMotor.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar motor del vehiculo");
            txtMotor.requestFocus();
            return;
        }
        if(txtCilindro.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar cilindro del vehiculo");
            txtCilindro.requestFocus();
            return;
        }
        if(txtColor.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar color del vehiculo");
            txtColor.requestFocus();
            return;
        }
        if(txtAnio.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar año del vehiculo");
            txtAnio.requestFocus();
            return;
        }
        if(txtPuerta.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el numero de puertas del vehiculo");
            txtPuerta.requestFocus();
            return;
        }
        if(txtPrecio.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el numero de puertas del vehiculo");
            txtPrecio.requestFocus();
            return;
        }
        vcarro dts = new vcarro();
        fcarro func = new fcarro();
        
        dts.setPlaca(txtPlaca.getText());
        dts.setMarca(txtMarca.getText());
        dts.setModelo(txtModelo.getText());
        dts.setMotor(Double.parseDouble(txtMotor.getText()));
        dts.setCilindro(Integer.parseInt(txtCilindro.getText()));
        dts.setColor(txtColor.getText());
        dts.setAnio(Integer.parseInt(txtAnio.getText()));
        dts.setPuertas(Integer.parseInt(txtPuerta.getText()));
        
        int seleccionado = JTipo.getSelectedIndex();
        dts.setTipo((String) JTipo.getItemAt(seleccionado));
        
        seleccionado = JEstado.getSelectedIndex();
        dts.setEstado((String) JEstado.getItemAt(seleccionado));
        
        dts.setPrecio(Double.parseDouble(txtPrecio.getText()));
        dts.setDescripcion(txtdesc.getText());
        
        
        
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El carro fue registrado exitosamente");
                mostrar("");
                inhabilitar();
            }
            
        }else if(accion.equals("editar")){
            dts.setIdcarro(Integer.parseInt(txtidecarro.getText()));
            
            if(func.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El carro fue editado exitosamente");
                mostrar("");
                inhabilitar();
            }
        }
        
        
    }//GEN-LAST:event_btguardarActionPerformed

